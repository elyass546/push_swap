#include "../push_swap_bonus.h"

int	ft_strcmp(char *s01, char *s02)
{
	int		i;

	i = 0;
	if (s01 == s02)
		return (1);
	while (s01[i] && s02[i])
	{
		if (s01[i] != s02[i])
			return (1);
		i++;
	}
	if ((s01[i] == '\0' && s02[i]) || (s01[i] && s02[i] == '\0'))
		return (1);
	return (0);
}
